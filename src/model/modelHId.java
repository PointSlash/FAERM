package model;

public class modelHId {
    private String id;
    private String password;

    public modelHId() {
    }

    public modelHId(String id, String password) {
        this.id = id;
        this.password = password;
    }

    // Getters and setters
    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
}
